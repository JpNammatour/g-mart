// Combined product list
export const allKeralaProducts: ProductData[] = [...keralaVegetables, ...keralaFruits]

// Helper functions
export const getProductsByCategory = (category: "vegetable" | "fruit"): ProductData[] => {
  return allKeralaProducts.filter((product) => product.category === category)
}

export const getSeasonalProducts = (): ProductData[] => {
  return allKeralaProducts.filter((product) => product.seasonal)
}

export const getOrganicProducts = (): ProductData[] => {
  return allKeralaProducts.filter((product) => product.organic)
}

export const searchProducts = (query: string): ProductData[] => {
  const lowercaseQuery = query.toLowerCase()
  return allKeralaProducts.filter(
    (product) =>
      product.name.toLowerCase().includes(lowercaseQuery) ||
      product.malayalamName.includes(query) ||
      product.description.toLowerCase().includes(lowercaseQuery),
  )
}
